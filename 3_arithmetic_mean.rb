# Напишите программу, которая находит среднее арифметическое трех, введенных пользователем целых чисел.

puts "Введіть перше число:"

a = gets.to_i

puts "Введіть друге число:"

b = gets.to_i

puts "Введіть третє число:"

c = gets.to_i

average = (a + b + c) / 3

puts "Середнє арифметичне трьох заданих чисел: " + average.to_s
